package exercicios.lista01;

public class Ex02RestoDaDivisao {

	public static void main(String[] args) {
		// Faça um programa que calcule o resto da divisão inteira entre dois números dados.
		// Exemplo: se dividirmos 25 por 4, temos resto=1
		
		int numeroA = 25;
		int numeroB = 4;
		int resto = 25 % 4;
		System.out.println("Resto da divisão de " + numeroA + " por " + numeroB + ": " + resto);

	}

}
